<script type="text/javascript">
    function Calculation() {
        var Money = document.getElementById("Money");
        var interest = document.getElementById("interest");
        var time = document.getElementById("time");
        var profit = document.getElementById("profit");
        var type = $("#selector").val();
        if (isNaN(Money.value)) {
            alert("模拟金额填写格式不正确");
            return false;
        }
        if (isNaN(interest.value)) {
            alert("模拟年化利率填写格式不正确");
            return false;
        }
        if (eval(interest.value) > 100) {
            alert("模拟年化利率填写格式不正确");
            return false;
        }
        if (isNaN(time.value)) {
            alert("模拟计息时长填写格式不正确");
            return false;
        }
        if (eval(time.value) > 360) {
            alert("请输入1~360之间");
            return false;
        }
        var s;
        if (type == "按月付息" || type == "到期还本息" || type == "季度付息" || type == "半年付息") {
            s = eval((eval(Money.value) * eval(interest.value) / 100 / 12)) / 30 * eval(time.value);
            profit.innerHTML = s.toFixed(2);
        } else {

            var x = Math.pow((1 + (eval(interest.value) / 100 / 12)), eval(time.value) / 30);
            var monthly = (eval(Money.value) * x * (eval(interest.value) / 100 / 12)) / (x - 1);
            s = (eval(time.value) / 30 * monthly) - eval(Money.value);
            profit.innerHTML = s.toFixed(2);
        }
    }
</script>
